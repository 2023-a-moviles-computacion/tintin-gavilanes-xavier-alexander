class Heladeria {
    var nombre: String =" ";
    var direccion: String  =" ";
    var telefono: String =" ";
    var calificacion: Double = 0.0
    var capacidad: Number = 0;

    constructor(nombre: String, direccion: String, telefono: String, calificacion: Double, capacidad: Number) {
        this.nombre = nombre
        this.direccion = direccion
        this.telefono = telefono
        this.calificacion = calificacion
        this.capacidad = capacidad
    }
}